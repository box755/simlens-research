"""Collect Bilibili danmaku TIMESTAMPS ONLY for v4.2.2 Anchor B-2.

Spec: SimLens_Research_Plan_v4.2.md §3.2 Step 1.4 Anchor B-2.
Academic basis: Refs [38] DanmakuTPPBench (NeurIPS 2025), [39] Plot-Aligned Danmaku.

v4.2.2 design: bili videos do NOT correspond to YouTube test set —
B-2 is a fully independent cross-platform distribution-level temporal anchor.

Input pairs file format simplified for v4.2.2 (no yt_id needed):
  {"bvid": "BV1xx...", "cid": <int>}
  one per line. Generated by collect_bilibili_popular.py.

Privacy/Legal: only second-level timestamps stored, no danmaku text.

Spec: SimLens_Research_Plan_v4.2.md §3.2 Step 1.4 Anchor B.
Privacy/Legal compliance: we deliberately drop danmaku text and preserve only
the per-video time offsets (in seconds). No personal speech is recorded.

Workflow:
  1. Read pairs file: data/anchor_b/yt_to_bili.jsonl
     each line:  {"yt_id": "...", "bvid": "BV1xx..."}
  2. For each BVID:
     (a) GET https://api.bilibili.com/x/web-interface/view?bvid={bvid}
         → extract first page's `cid`
     (b) GET https://comment.bilibili.com/{cid}.xml
         → parse XML, extract timestamps from each <d> element's p attribute
         → strip text content (only keep float timestamp seconds)
  3. Write data/anchor_b/danmaku_timestamps/{yt_id}.json
     {"yt_id": ..., "bvid": ..., "cid": ..., "n_danmaku": N,
      "timestamps": [12.34, 18.7, 45.1, ...]}

Pairs file (yt_to_bili.jsonl) format — created manually or by script:
  {"yt_id": "8uFR6-yULcU", "bvid": "BV1xx411c123"}

Without a pairs file, this script cannot proceed (Bilibili search API has
strong rate limits and the search-by-title quality is too low to automate).

Usage:
  python scripts/data_collection/collect_bilibili_danmaku.py \
    --pairs data/anchor_b/yt_to_bili.jsonl \
    --out-dir data/anchor_b/danmaku_timestamps
"""

from __future__ import annotations

import argparse
import json
import re
import sys
import time
import urllib.parse
import urllib.request
import xml.etree.ElementTree as ET
from pathlib import Path

USER_AGENT = (
    "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 "
    "(KHTML, like Gecko) Chrome/130.0.0.0 Safari/537.36"
)


def http_get(url: str, timeout: int = 30) -> bytes:
    req = urllib.request.Request(url, headers={"User-Agent": USER_AGENT})
    with urllib.request.urlopen(req, timeout=timeout) as resp:
        return resp.read()


def bvid_to_cid(bvid: str) -> tuple[int, int] | None:
    """BVID → (cid, duration_sec). Returns None if API rejects."""
    url = f"https://api.bilibili.com/x/web-interface/view?bvid={bvid}"
    raw = http_get(url)
    data = json.loads(raw)
    if data.get("code") != 0:
        print(f"  view API rejected bvid={bvid}: {data.get('message')}",
              file=sys.stderr)
        return None
    info = data["data"]
    cid = info["cid"]  # main page cid
    duration = info.get("duration", 0)
    return cid, duration


def fetch_danmaku_xml(cid: int) -> bytes:
    """Fetch raw XML (deflate-compressed by server). We use the unencrypted
    legacy endpoint which is publicly accessible.
    """
    url = f"https://comment.bilibili.com/{cid}.xml"
    return http_get(url)


def parse_timestamps_only(xml_bytes: bytes) -> list[float]:
    """Parse <d p="time,...,...,..."> entries, return ONLY the time field.

    Bilibili's comment.bilibili.com/{cid}.xml endpoint serves payload
    deflate-compressed (raw zlib, no header). Handle plaintext + raw deflate
    + standard zlib variants.
    """
    import zlib

    text: str | None = None
    try:
        candidate = xml_bytes.decode("utf-8")
        if "<i>" in candidate or "<d " in candidate:
            text = candidate
    except UnicodeDecodeError:
        pass
    if text is None:
        try:
            text = zlib.decompress(xml_bytes, -zlib.MAX_WBITS).decode("utf-8", errors="ignore")
        except zlib.error:
            pass
    if text is None:
        try:
            text = zlib.decompress(xml_bytes).decode("utf-8", errors="ignore")
        except zlib.error:
            pass
    if text is None or "<d " not in text:
        return []

    timestamps: list[float] = []
    try:
        root = ET.fromstring(text)
    except ET.ParseError as e:
        print(f"  XML parse error: {e}", file=sys.stderr)
        return []
    for d in root.findall("d"):
        p = d.get("p", "")
        if not p:
            continue
        try:
            t = float(p.split(",", 1)[0])
            if t >= 0:
                timestamps.append(t)
        except ValueError:
            continue
    return timestamps


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--pairs", required=True,
                    help="JSONL file mapping yt_id -> bvid")
    ap.add_argument("--out-dir", required=True)
    ap.add_argument("--sleep", type=float, default=0.5,
                    help="seconds to wait between Bilibili API calls "
                         "(default 0.5; bump up if rate-limited)")
    args = ap.parse_args()

    pairs: list[dict] = []
    with Path(args.pairs).open(encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line or line.startswith("#"):
                continue
            pairs.append(json.loads(line))
    print(f"Loaded {len(pairs)} yt→bili pairs from {args.pairs}")

    out_dir = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    n_done = n_skip = n_fail = 0
    for pair in pairs:
        yt_id = pair["yt_id"]
        bvid = pair["bvid"]
        out_path = out_dir / f"{yt_id}.json"
        if out_path.exists():
            n_skip += 1
            continue

        # Step 1: BVID → cid
        try:
            cid_info = bvid_to_cid(bvid)
        except Exception as e:
            print(f"  {yt_id}/{bvid}: view API failed: {type(e).__name__}: {e}")
            n_fail += 1
            continue
        if cid_info is None:
            n_fail += 1
            continue
        cid, bili_duration = cid_info
        time.sleep(args.sleep)

        # Step 2: cid → danmaku XML → timestamps only
        try:
            xml_bytes = fetch_danmaku_xml(cid)
        except Exception as e:
            print(f"  {yt_id}/{bvid}: danmaku fetch failed: {type(e).__name__}: {e}")
            n_fail += 1
            continue

        timestamps = parse_timestamps_only(xml_bytes)
        if not timestamps:
            print(f"  {yt_id}/{bvid}: 0 timestamps parsed (empty or rate-limited)")
            n_fail += 1
            continue

        rec = {
            "yt_id": yt_id,
            "bvid": bvid,
            "cid": cid,
            "bili_duration_sec": bili_duration,
            "n_danmaku": len(timestamps),
            "timestamps": timestamps,
            "_disclaimer": (
                "Only second-level timestamps are stored. Danmaku TEXT is "
                "deliberately NOT recorded for privacy/legal compliance "
                "(SimLens v4.2 §3.2 Step 1.4 Anchor B)."
            ),
        }
        out_path.write_text(json.dumps(rec, ensure_ascii=False, indent=2),
                            encoding="utf-8")
        n_done += 1
        print(f"  [{n_done}/{len(pairs)}] {yt_id} ↔ {bvid}: "
              f"{len(timestamps)} danmaku timestamps "
              f"(yt: ?s, bili: {bili_duration}s)")
        time.sleep(args.sleep)

    print(f"\nSummary: done={n_done} skip={n_skip} fail={n_fail}")


if __name__ == "__main__":
    main()
