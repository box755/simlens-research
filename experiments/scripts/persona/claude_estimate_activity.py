"""Fill expected_comment_count_range for each sampled persona via Claude zero-shot.

Spec: SimLens_Research_Plan_v4.1.md §2.3 Step 5.
  Range is per 2-minute baseline (R_frequency_match scales by duration_factor).

Usage:
  python scripts/persona/claude_estimate_activity.py \
    --in  data/personas/personas_sampled.yaml \
    --out data/personas/personas_with_activity.yaml
"""

from __future__ import annotations

import argparse
import json
import os
import re
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parents[1]))
from utils.env import load_env

load_env()


PROMPT_TEMPLATE = """You are estimating how many YouTube comments a viewer with this persona would post on a typical 2-minute video.

Persona description:
{persona}

Based on the persona's interests, personality, and likely engagement style, estimate a tight low-high range of comments they would post on a 2-minute YouTube video.

Conventions:
- Introverted, analytical, or low-engagement personas: (0, 1) or (0, 2)
- Average engagement: (1, 3)
- Extroverted, expressive, or high-engagement personas: (3, 5) or (3, 6)
- Very enthusiastic / meme-heavy / chronically online: (4, 7)

Output ONLY a JSON object: {{"low": <int>, "high": <int>, "rationale": "<one sentence>"}}
"""


def call_claude(prompt: str, model: str = "claude-sonnet-4-5-20250929") -> str:
    from anthropic import Anthropic  # type: ignore

    client = Anthropic()
    msg = client.messages.create(
        model=model,
        max_tokens=200,
        messages=[{"role": "user", "content": prompt}],
    )
    return msg.content[0].text


def parse_yaml_personas(text: str) -> list[dict]:
    """Minimal YAML parser tailored for sample_personachat.py output (no full PyYAML dep)."""
    out: list[dict] = []
    cur: dict | None = None
    desc_lines: list[str] | None = None
    for raw in text.splitlines():
        line = raw.rstrip()
        if not line or line.lstrip().startswith("#"):
            continue
        m = re.match(r"^(P\d+):\s*$", line)
        if m:
            if cur:
                if desc_lines is not None:
                    cur["description"] = "\n".join(desc_lines).rstrip()
                out.append(cur)
            cur = {"id": m.group(1)}
            desc_lines = None
            continue
        if cur is None:
            continue
        if line.startswith("  source_idx:"):
            cur["source_idx"] = int(line.split(":", 1)[1].strip())
        elif line.startswith("  query_similarity:"):
            cur["query_similarity"] = float(line.split(":", 1)[1].strip())
        elif line.startswith("  description:"):
            desc_lines = []
        elif line.startswith("  expected_comment_count_range:"):
            if desc_lines is not None:
                cur["description"] = "\n".join(desc_lines).rstrip()
                desc_lines = None
        elif line.startswith("    ") and desc_lines is not None:
            desc_lines.append(line[4:])
    if cur:
        if desc_lines is not None:
            cur["description"] = "\n".join(desc_lines).rstrip()
        out.append(cur)
    return out


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--in", dest="inp", required=True)
    ap.add_argument("--out", required=True)
    ap.add_argument("--model", default="claude-sonnet-4-5-20250929")
    args = ap.parse_args()

    if "ANTHROPIC_API_KEY" not in os.environ:
        print("ERROR: ANTHROPIC_API_KEY not set", file=sys.stderr)
        sys.exit(2)

    personas = parse_yaml_personas(Path(args.inp).read_text())
    print(f"Estimating activity for {len(personas)} personas via {args.model}")

    for p in personas:
        prompt = PROMPT_TEMPLATE.format(persona=p["description"])
        raw = call_claude(prompt, model=args.model).strip()
        # Pull out the JSON object even if Claude wraps it
        m = re.search(r"\{.*\}", raw, re.DOTALL)
        if not m:
            raise RuntimeError(f"{p['id']}: no JSON found in {raw!r}")
        obj = json.loads(m.group(0))
        p["expected_comment_count_range"] = [int(obj["low"]), int(obj["high"])]
        p["activity_rationale"] = obj.get("rationale", "")
        print(f"  {p['id']}: ({obj['low']}, {obj['high']}) — {obj.get('rationale','')[:80]}")

    out_path = Path(args.out)
    out_path.parent.mkdir(parents=True, exist_ok=True)
    lines = [
        "# Auto-generated by claude_estimate_activity.py — do NOT hand-edit",
        f"# Activity estimator: {args.model}",
        "",
    ]
    for p in personas:
        lines.append(f"{p['id']}:")
        lines.append(f"  source_idx: {p['source_idx']}")
        lines.append(f"  query_similarity: {p['query_similarity']:.4f}")
        lines.append(f"  description: |")
        for s in p["description"].split("\n"):
            lines.append(f"    {s}")
        lo, hi = p["expected_comment_count_range"]
        lines.append(f"  expected_comment_count_range: [{lo}, {hi}]  # per 2-min baseline")
        lines.append(f"  activity_rationale: {json.dumps(p['activity_rationale'])}")
        lines.append("")
    out_path.write_text("\n".join(lines))
    print(f"\nWrote {out_path}")


if __name__ == "__main__":
    main()
